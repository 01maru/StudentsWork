#include "PlayerSkill.h"

//-----------------------------------------------------------------------------
// [SECTION] Update
//-----------------------------------------------------------------------------

void PlayerSkill::Initialize()
{
	isActive_ = TRUE;
}

void PlayerSkill::Update()
{
	sprite_.Update();
	text_.Update();
}

//-----------------------------------------------------------------------------
// [SECTION] Draw
//-----------------------------------------------------------------------------

void PlayerSkill::Draw()
{
	sprite_.Draw();
	text_.Draw();
}

//-----------------------------------------------------------------------------
// [SECTION] Getter
//-----------------------------------------------------------------------------

bool PlayerSkill::GetIsActive()
{
	return isActive_;
}

//-----------------------------------------------------------------------------
// [SECTION] Setter
//-----------------------------------------------------------------------------

void PlayerSkill::SetSprite(const MNE::Sprite& sprite, const MNE::Sprite& text)
{
	sprite_ = sprite;
	text_ = text;
}
